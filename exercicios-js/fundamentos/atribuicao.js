// Operador de Atribuição e variações

const a = 7; // A utilização do "=" serve para atribuir valores a essa constante

let b = 3; // Aqui é do mesmo jeito foi realizado uma atribuição do valor a uma variável

//Atribuição aditiva 
b += a; // b = b + a

console.log(b);

//Atribuição subtrativa
b -= 4; // b = b - 4 
console.log(b);

//Atribuição multiplicativa
b *= 2; // b = b * 2 
console.log(b);

//Atribuição divisiva
b /= 2; // b = b / 2 
console.log(b);

//Atribuição do resto da divisão
b %= 2; // b = b % 2 
console.log(b);
